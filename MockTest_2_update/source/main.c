/*******************************************************************************
 * Includes
 ******************************************************************************/
#include "MKL46Z4.h"
#include "app.h"
#include "gpio.h"
/*******************************************************************************
 * Codes
 ******************************************************************************/
int main(void)
{
    LED_Init();
    SW_Init();
    /* Check button */
    if((GPIOC->PDIR &(1<<3)) == 0)
    {  /* Call boot app */
        Boot();
    }
    else
    {  /* call new App */
        App();
    }
}
